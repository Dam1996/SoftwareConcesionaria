    private void loginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnMouseClicked
        // TODO add your handling code here:
        String userValido = "admin";
        String passValida = "12345";

        String userIngresado = nombreUsuarioLb.getText();
        String passwordIngresada = new String(passwordText.getPassword());

        if (userIngresado.equals(userValido) && passwordIngresada.equals(passValida)) {

            AdministradorView ventana = new AdministradorView();
            ventana.setSize(800,700);
            ventana.setVisible(true);
            JFrame jf= new JFrame();        //JFrame me permite ejecutar la ventana.
            jf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            jf.setSize(1350, 600);
            jf.add(ventana);
            jf.setVisible(true);
            dispose();

        } else {
            errorLabel.setVisible(true);
        }
    }//GEN-LAST:event_loginBtnMouseClicked

